


function show_list(){
    document.getElementById('nav_list').style.display="block"
    document.getElementById('show_list').style.display="none"
    document.getElementById('close_list').style.display="block"
    
}

function close_list(){
    document.getElementById('nav_list').style.display="none"
    document.getElementById('show_list').style.display="block"
    document.getElementById('close_list').style.display="none"

}